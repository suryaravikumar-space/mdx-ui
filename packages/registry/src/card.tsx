export interface LinkCardProps extends React.AnchorHTMLAttributes<HTMLAnchorElement> {
  title: string;
  description?: string;
}

export const LinkCard = React.forwardRef<HTMLAnchorElement, LinkCardProps>(
  ({ title, description, className, children, ...props }, ref) => (
    <a
      ref={ref}
      className={cn(
        "group block rounded-lg border border-border bg-card p-6 text-card-foreground shadow-sm transition-colors hover:bg-accent hover:text-accent-foreground no-underline",
        className,
      )}
      {...props}
    >
      <div className="font-semibold">{title}</div>
      {description && (
        <div className="mt-1 text-sm text-muted-foreground group-hover:text-accent-foreground/80">
          {description}
        </div>
      )}
      {children}
    </a>
  ),
);
LinkCard.displayName = "LinkCard";
